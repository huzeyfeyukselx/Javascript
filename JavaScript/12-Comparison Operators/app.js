
// ---------------------------------------

// Comparison Operators

// (==)
console.log(2==2); // out : true
console.log("Java"=="C++"); // out : false
console.log(2=="2"); // out : true
// (===)
console.log(2==="2"); // out : false
// (!=)
console.log(2!=4); // out : true
console.log(2!=2); // out : false
// (>)
console.log(5>4); // out : true
// (<)
console.log(5<4); // out : false
// (>=)
console.log(5>=5); // out : true
// (<=)
console.log(5<=5); // out : true

// ---------------------------------------

// Logical Operators

// NOT (!)
console.log(!(2==2)); // out : false

// AND (&&)
// T	T	T
// T	F	F
// F	T	F
// F	F	F
console.log((2==2) && ("Ahmet"=="Ahmet")); // out : true

// OR (||)
// T	T	T
// T	F	T
// F	T	T
// F	F	F
console.log((2==2) || ("Ahmet"=="Mehmet")); // out : true

