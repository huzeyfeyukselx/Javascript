let number =3;

// if-else
if(number==1){
    console.log("One!");
}else if(number==2){
    console.log("Two");
}else if(number==3){
    console.log("Three");
}else{
    console.log("Default");
}

//switch-case
switch(number){
    case 1: console.log("One");break;
    case 2: console.log("Two");break;
    case 3: console.log("Three");break;
    default: console.log("Default");break;
}