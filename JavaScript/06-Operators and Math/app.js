let value; 
const value1=10;
const value2=4;

//----------------------------------------------------
// OPERATORS :

value = value1 + value2;
console.log(value); // out : 14

value = value1 - value2;
console.log(value); // out : 6

value = value1 * value2;
console.log(value); // out : 40

value = value1 / value2;
console.log(value); // out : 2.5

value = value1 % value2;
console.log(value); // out : 2

//----------------------------------------------------
// MATH :

value = Math.PI;
console.log(value); // out : 3.141592653589793

value = Math.round(3.5)
console.log(value); // out : 4

value = Math.round(3.4)
console.log(value); // out : 3

value = Math.ceil(3.4)
console.log(value); // out : 4

value = Math.floor(3.4)
console.log(value); // out : 3

value = Math.floor(3.4)
console.log(value); // out : 3

value = Math.sqrt(16)
console.log(value); // out : 4

value = Math.abs(-10)
console.log(value); // out : 10

value = Math.pow(2,3)
console.log(value); // out : 8

value = Math.max(-2,3,-100,65,37)
console.log(value); // out : 65

value = Math.min(-2,3,-100,65,37)
console.log(value); // out : -100

value = Math.random()
console.log(value); // out : 0-1

value = Math.random()*20
console.log(value); // out : 0-20

value = Math.random()*20+1
console.log(value); // out : 1-20